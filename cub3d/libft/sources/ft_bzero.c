#include "libft.h"

void	*ft_bzero(void *ptr, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *) ptr;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
	return (ptr);
}
